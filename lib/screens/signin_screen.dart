                          FaIcon(FontAwesomeIcons.google),
                          FaIcon(FontAwesomeIcons.apple),
                        ],
                      ),
                      const SizedBox(
                        height: 25.0,
                      ),
                      // don't have an account
                      Row(
                        mainAxisAlignment: MainAxisAlignment.center,
                        children: [
                          const Text(
                            'Tidak Memiliki Akun? ',
                            style: TextStyle(
                              color: Colors.black45,
                            ),
                          ),
